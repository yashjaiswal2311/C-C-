									/*Unsigned short int (2byte)
									  limit is 0 to 65535*/									
#include <iostream>
using namespace std;
int main()
{
	unsigned short c=0;
	unsigned short int d=65535;

	cout<<"c = "<<c<<endl;
	cout<<"d = "<<d<<endl;
	return 0;
}


//if we want to make any variable unsigned then we need to mention unsigned keyword other by default it will 
//take it as signed.
